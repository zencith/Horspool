def codeToText(code): 
    decoded = '' 
    mors = '' 
    for i in code:  
        if (i != ' '):  
            cnt = 0
            mors += i  
        else: 
            cnt += 1
            if cnt == 2 : 
                decoded += ' '
            else:  
                decoded += list(ARRAY.keys())[list(ARRAY .values()).index(mors)] 
                mors = '' 
    return decoded
